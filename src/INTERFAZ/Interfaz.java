    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed
        // BOTON EJECUTAR
        Util.Utilidades.eliminarTodosLosConjuntos();
        Util.Utilidades.eliminarTodasLasOperaciones();
        Util.Utilidades.eliminarTodasLasEvaluaciones();
        Util.Utilidades.borrarErrores();
        Util.Utilidades.borrarTokens();

        MyTab t = (MyTab) pestañas.getSelectedComponent();
        String texto = t.getText();

        Lexico lex = new Lexico(new BufferedReader(new StringReader(texto))); // Instanciar el analizador léxico
        Parser parser = new Parser(lex);
        try {
            parser.parse();
            System.out.println("====================================");
            // Util.Utilidades.imprimirConjuntos();
            // Util.Utilidades.imprimirOperaciones();
            // Util.Utilidades.imprimirEvaluaciones();
            Util.Ejecutar.Ejecutar();
            jTextArea1.setText("Analisis completo exitoso");
            jTextArea1.setText(Util.Ejecutar.SalidaConsola());
            Util.PanelGrafica.graficarOperaciones(this);
        } catch (Exception e) {
            jTextArea1.setText(
                    "Error fatal en compilación de entrada.\nCausa: " + e.getCause() + "\nMensaje: " + e.getMessage());
        }

    }// GEN-LAST:event_jButton1ActionPerformed

