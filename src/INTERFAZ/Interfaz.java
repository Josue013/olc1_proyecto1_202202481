    public void mostrarGraficoActual() {
        if (panelesGraficos.isEmpty()) {
            return;
        }

        jPanel1.removeAll(); // Limpiar el panel antes de agregar el nuevo gráfico
        PanelGrafica panelActual = panelesGraficos.get(indiceGraficoActual);
        jPanel1.add(panelActual, BorderLayout.CENTER); // Agregar el gráfico actual al panel
        jPanel1.revalidate(); // Refrescar el panel para mostrar los cambios
        jPanel1.repaint();
    }

