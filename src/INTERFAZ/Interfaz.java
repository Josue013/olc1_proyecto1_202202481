    // Guardar archivo
    private void GuardarArchivo() {
        MyTab t = (MyTab) pestañas.getSelectedComponent();
        if (t.getPath() != null) {
            Archivo.GuardarArchivo(t.getText(), t.getPath());
        } else {
            GuardarComoArchivo();
        }
    }

    // Guardar como archivo
    private void GuardarComoArchivo() {
        if (chooser.showSaveDialog(this) == JFileChooser.APPROVE_OPTION) {
            File archivo = chooser.getSelectedFile();
            if (!archivo.getName().endsWith(".ca")) {
                archivo = new File(archivo.getAbsolutePath() + ".ca"); // Agregar extensión

            }
            String texto = ((MyTab) pestañas.getSelectedComponent()).getText(); // Obtener texto

            // colocar titulo
            pestañas.setTitleAt(pestañas.getSelectedIndex(), new File(archivo.getAbsolutePath()).getName());
            Archivo.GuardarArchivo(texto, archivo.getAbsolutePath());
        }
    }

